// Build lines for each language
function buildBlock(langIdx) {
  const lines = [];
  let currentGroup = '';
  const groupMap = {
    'home': 'Home page', 'analytics': 'Analytics page', 'explorer': 'Explorer page',
    'health': 'Health page', 'transactions': 'Transactions page', 'nav': 'Navbar (additional)',
    'auth': 'Auth Modal',
  };
  for (const [key, vals] of Object.entries(newKeys)) {
    const prefix = key.split('.')[0];
    const group = groupMap[prefix] || '';
    if (group !== currentGroup) {
      if (currentGroup) lines.push('');
      lines.push(`    // ── ${group} ────────────────────────────────────────`);
      currentGroup = group;
    }
    const val = vals[langIdx];
    if (!val) {
      console.error(`Missing translation for ${key} at index ${langIdx}`);
      process.exit(1);
    }
    lines.push(`    "${key}": "${val.replace(/"/g, '\\"')}",`);
  }
  return lines.join('\n');
}

